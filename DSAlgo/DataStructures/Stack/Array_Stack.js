let stack = [];
/** 
This method is used to push the element to last
*/
stack.push(5);
/**
 This method is used to remove from last of stack 
 */
stack.pop();

//* we can also use the shift and unshift method
//*But it will not recommoned
//* Since we need to reindex the whole array.
stack.unshift(5);
stack.shift();
